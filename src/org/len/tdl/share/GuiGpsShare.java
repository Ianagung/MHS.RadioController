    private void GUI_addPortList() {
        jComboBoxGpsPort.removeAllItems();
        String[] portNames = SerialPortList.getPortNames();
        for (String portName : portNames) {
            jComboBoxGpsPort.addItem(portName);
        }
    }

    private class SerialGpsParserReceive implements DataListener {

        @Override
        public void dataReceive(byte[] aData, int aType) {
            mainTxGps1.sendGpsGprmc(aData, Integer.valueOf(jTextFieldIpPortUdpGps1.getText()), jTextFieldIpAddressUdpGps1.getText());
            mainTxGps2.sendGpsGprmc(aData, Integer.valueOf(jTextFieldIpPortUdpGps2.getText()), jTextFieldIpAddressUdpGps2.getText());

            if (jCheckBoxLoggerGpsEnable.isSelected()) {
                jTextAreaLoggerGps.append(new String(aData) + "\n");
            }
        }

        @Override
        public void infoReceive(byte[] track) {
            //System.out.println("SEND OWN TRACK GPS TO CCD");
        }
    }

