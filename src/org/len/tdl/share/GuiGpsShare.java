    private void jButtonGpsConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGpsConnectActionPerformed

        if (mainRxGps.isGpsConnect()) {
            mainRxGps.removeListener();
            mainRxGps.disconnectGps();
        } else {
            mainRxGps.addListener(new SerialGpsParserReceive());
            mainRxGps.connectGps();
        }

        if (mainRxGps.isGpsConnect()) {
            jButtonGpsConnect.setText("Disconnect");
            jLabelStatusGps.setText("CONNECTED");
            jLabelStatusGps.setForeground(Color.blue);
        } else {
            jButtonGpsConnect.setText("Connect");
            jLabelStatusGps.setText("NOT CONNECTED");
            jLabelStatusGps.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButtonGpsConnectActionPerformed

